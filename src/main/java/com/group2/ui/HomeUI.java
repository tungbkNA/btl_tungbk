    public void soSpBanTrongThang(List<ThongKe> list) {
        dlmList2.removeAllElements();
        for (ThongKe tk : list) {
            dlmList2.addElement(new ProductHot(tk.getMaSP(), tk.getTenSP(), tk.getSoLuong()));
        }
        card2.setTextJlabel(String.valueOf(list.size()));

    }

    public void HVHetHan(List<ThongKe> list) {
        dlmList3.removeAllElements();
        if (list.size() > 0) {
            for (ThongKe tk : list) {
                dlmList3.addElement(new HoiVienDateTime(tk.getHoTen(), tk.getSoDT(), GDate.toDate(tk.getNgayHetHan(), "yyyy-MM-dd")));
            }
        }
        card3.setTextJlabel(String.valueOf(list.size()));
    }


    private void fillToList() {
        dlmList1.removeAllElements();
        for (ChekIn chekIn : checkInDAO.SoHVCheckIn()) {
            dlmList1.addElement(chekIn.toString());
        }
        card4.setTextJlabel(checkInDAO.SoHVCheckIn().size() + "");

    }

